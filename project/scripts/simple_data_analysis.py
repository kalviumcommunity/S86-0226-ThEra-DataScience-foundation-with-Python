# Simple Data Analysis Script
# This script demonstrates basic data logic and printing results.

# Define variables
numbers = [10, 20, 30, 40, 50]

# Calculate the sum and average
sum_numbers = sum(numbers)
average = sum_numbers / len(numbers)

# Print results
print("Numbers:", numbers)
print("Sum:", sum_numbers)
print("Average:", average)

# Brief explanation
print("\nThis script runs from top to bottom, calculates the sum and average of a list, and prints the results. Scripts are useful for automation and repeatable analysis.")
